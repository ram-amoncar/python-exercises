# Simple Calculator

a = 10
b = 5

print("a + b =", end=" ")
print(a + b)

print("a - b =", end=" ")
print(a - b)

print("a * b =", end=" ")
print(a * b)

print("a / b =", end=" ")
print(a / b)

print("a // b =", end=" ")
print(a // b, end=" ")
